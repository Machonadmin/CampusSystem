/**
 * Убирает спецсимволы синтаксиса PostgREST-фильтра из пользовательского поиска,
 * который подставляется в строку `.or(\`col.ilike.%${v}%\`)`.
 *
 * PostgREST разбирает .or() по запятым и скобкам, а ilike использует % и *; без
 * очистки значение из ?q=/?search= могло бы инъектировать доп. условия фильтра
 * (напр. закрыть скобку и дописать своё). Заменяем эти символы пробелом.
 *
 * Значения, передаваемые ОТДЕЛЬНЫМ аргументом (`.ilike('col', value)`), в
 * очистке не нуждаются — там нет разбора синтаксиса; эта функция только для
 * случая интерполяции в строку фильтра.
 */
export function sanitizeOrSearch(raw: string | null | undefined): string {
  return (raw ?? '').replace(/[%,()*\\]/g, ' ').trim()
}

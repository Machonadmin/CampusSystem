-- Роли учёбы: убираем ДУБЛЬ, наведённый сегодня (владелец 2026-07-15).
--
-- Оказалось, в системе УЖЕ есть точные роли иерархии (RU-названия, права
-- education настроены в 002_roles_and_privileges + 20260511175354):
--   Ректор(rector) · Декан(dean) · Директор учебного заведения(school_director) ·
--   Заместитель директора(vice_director) · Преподаватель(teacher) ·
--   Студент(student)/Ученик(pupil) · head_of_studies («ответственный за учёбу»).
--
-- Моя миграция 20260715120000 создала ГЕНЕРИК-роль studies_manager — дубль
-- этой структуры. Никому не назначена (владелец проверил: 0 строк) → удаляем.
-- studies_secretary ОСТАВЛЯЕМ как генерик-секретаря (её использует панель
-- состава единицы; уже переименована в «Секретарь учёбы» в 20260715280000).
--
-- Плюс head_of_studies был назван на иврите → даём русское имя по умолчанию.
--
-- RLS отключён. Применять ВРУЧНУЮ. Транзакция.
-- ═════════════════════════════════════════════════════════════════════

BEGIN;

-- studies_manager — дубль, никому не назначен. Сначала зависимые строки.
DELETE FROM role_privileges    WHERE role_id IN (SELECT id FROM roles WHERE code = 'studies_manager');
DELETE FROM role_module_access WHERE role_id IN (SELECT id FROM roles WHERE code = 'studies_manager');
DELETE FROM roles WHERE code = 'studies_manager';

-- head_of_studies — русское имя/описание по умолчанию (было на иврите).
UPDATE roles SET
  name = 'Ответственный за учёбу',
  description = 'Ответственный за учебную единицу (кодеш/учёба): видит всех, управляет своей'
  WHERE code = 'head_of_studies';

COMMIT;
